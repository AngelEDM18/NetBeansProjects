package modelo;

/**
 *
 * @author alumno_2DAW
 */
public class Entrante {

    int id;
    int tipo;
    String descripcion;

    public Entrante(int id, int tipo, String descripcion) {
        this.setId(id);
        this.setTipo(tipo);
        this.setDescripcion(descripcion);
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
}
