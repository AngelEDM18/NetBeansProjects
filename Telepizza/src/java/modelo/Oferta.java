package modelo;

/**
 *
 * @author alumno_2DAW
 */
public class Oferta {

    int id;
    String descripcion;

    public Oferta(int id, String descripcion) {
        this.setId(id);
        this.setDescripcion(descripcion);
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
}
